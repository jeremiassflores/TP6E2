    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        modelo.setRowCount(0);
        for (Producto aux : JfrmMenuPrincipal.listaProductos){
            String descripIngresada = jTextField1.getText();
            String descripGuardada = String.valueOf(aux.getDescripcion());
            if (descripGuardada.toLowerCase().startsWith(descripIngresada.toLowerCase())) {
                modelo.addRow(new Object[]{aux.getCodigo(), aux.getDescripcion(), aux.getPrecio(), aux.getRubro(), aux.getStock()});
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased


